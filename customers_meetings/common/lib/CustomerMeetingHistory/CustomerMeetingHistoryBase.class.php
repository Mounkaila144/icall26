<?php

class CustomerMeetingHistoryBase extends mfObject2 {
     
    protected static $fields=array('customer_id','user_id','user_application','history','created_at','updated_at');
    const table="t_customers_meeting_history"; 
    protected static $foreignKeys=array('customer_id'=>'Customer','user_id'=>'User',
                                        'old_status_id'=>'CustomerMeetingStatus','new_status_id'=>'CustomerMeetingStatus'); // By default
 
                
    function __construct($parameters=null,$site=null) {
      parent::__construct(null,$site);   
      $this->getDefaults(); 
      if ($parameters === null)  return $this;      
      if (is_array($parameters)||$parameters instanceof ArrayAccess)
      {
          if (isset($parameters['id']))
             return $this->loadbyId((string)$parameters['id']); 
          return $this->add($parameters); 
      }   
      else
      {
         if (is_numeric((string)$parameters)) 
            return $this->loadbyId((string)$parameters);
         
      }   
    }
    
