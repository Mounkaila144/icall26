    function setI18n($i18n)
    {
        $this->i18n=$i18n;
        return $this;
    }
    
     function getI18n($lang=null)
    {
        if ($this->i18n===null)
        {
            if ($lang==null)
                $lang=mfContext::getInstance()->getUser()->getCountry();
            $this->i18n=new UserFunctionI18n(array('lang'=>$lang,'function_id'=>$this->get('id')),$this->getSite());
        }    
        return $this->i18n;
    }       
       
}
