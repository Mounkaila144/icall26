    function getUserAttributionI18n($lang=null)
    {
        if (!$this->user_attribution_i18n)
        {
            if ($lang==null)
                $lang=mfContext::getInstance()->getUser()->getCountry();
            $this->user_attribution_i18n=new UserAttributionI18n(array('lang'=>$lang,'attribution_id'=>$this->get('id')),$this->getSite());
